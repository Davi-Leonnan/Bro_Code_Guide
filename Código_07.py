# Funções Para um Nome:

Nome = "Max Pane"

print(len(Nome))  # Número de letras em um nome.
print(Nome.find(Nome))  # Inicio de um nome.
print(Nome.capitalize())  # Replica o nome.
print(Nome.upper())  # Deixa todo o nome em Maisculo
print(Nome.lower())  # Deixa todo o nome em minusculo
print(Nome.isdigit())  # Registra números, caso não tenha, permaneçe Falso.
print(Nome.isalpha())  # Registra um caractere que não seja letra, caso não, permaneçe Falso.
print(Nome.count("a")) # Registra um certo caractere presente no nome.
print(Nome.replace("P", "L"))  # Troca o caractere de uma letra.
print(Nome * 100)  # Repete o nome N vezes.


